#!/usr/bin/env python3
'''1-concat.py
'''


def concat(str1: str, str2: str) -> str:
    """A typed-annotated function that concatenates 2 strings

    Args:
        str1 (str): string one
        str2 (str): string two

    Returns:
        str: A concatenated string
    """
    return str1 + str2
